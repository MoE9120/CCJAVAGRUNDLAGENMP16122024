package Thema1_Grundlage;

import java.io.IO;

public class BerechnungenFloat {

	public static void main(String[] args) {
		float i = 15.45f;
		float y = 13.32f;
		float ergebnis1 = i + y;
		float ergebnis2 = i - y;
		float ergebnis3 = i / y;
		float ergebnis4 = i * y;
		float ergebnis5 = i % y;
		
		
		
		System.out.println("Additionsergebnis: " + ergebnis1);
		System.out.println("Subtraktionsergebnis: " + ergebnis2);
		System.out.println("Divisionsionsergebnis: " + ergebnis3);
		System.out.println("Multiplikationssergebnis: " + ergebnis4);
		System.out.println("Modulo: " + ergebnis5);
		
	}

}
