package Thema2_Datentypen;

public class ASCII_HelloWorld {

	public static void main(String[] args) {
		String text = "Hello World";
		
		System.out.println("ASCII-Werte von 'Hello World':");
        System.out.println(text.charAt(0) + ": " + (int) text.charAt(0));
        System.out.println(text.charAt(1) + ": " + (int) text.charAt(1));
        System.out.println(text.charAt(2) + ": " + (int) text.charAt(2));
        System.out.println(text.charAt(3) + ": " + (int) text.charAt(3));
        System.out.println(text.charAt(4) + ": " + (int) text.charAt(4));
        System.out.println(text.charAt(5) + ": " + (int) text.charAt(5));
        System.out.println(text.charAt(6) + ": " + (int) text.charAt(6));
        System.out.println(text.charAt(7) + ": " + (int) text.charAt(7));
        System.out.println(text.charAt(8) + ": " + (int) text.charAt(8));
        System.out.println(text.charAt(9) + ": " + (int) text.charAt(9));
        System.out.println(text.charAt(10) + ": " + (int) text.charAt(10));

	}

}
