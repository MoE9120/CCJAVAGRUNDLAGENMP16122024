package Thema3_Verzweigungen;

public class IfElse {

	public static void main(String[] args) {
		
		int zahl = 10;
		if(zahl > 5) {
			System.out.println("Die Zahl ist größer als 5.");	
		} else if (zahl == 10) {
			System.out.println("Die Zahl ist 5");
		}
		  else {
			System.out.println("Die Zahl ist größer als 5");
	}

	}

}
